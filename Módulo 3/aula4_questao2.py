nota = int(input ("Que nota de 1 a 5 você dá para o filme? "))

if nota == 1:
    print ("Ruim.")

elif nota == 2:
    print ("Regular.")

elif nota == 3:
    print ("Bom!")

elif nota == 4:
    print ("Muito bom!")

elif nota == 5:
    print ("Excelente!")

else:
    print ("Nota inválida. Diga uma nota de 1 a 5!")